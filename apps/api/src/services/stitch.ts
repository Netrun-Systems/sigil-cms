/**
 * Stitch Service — wraps Google Stitch design generation API.
 *
 * When STITCH_API_KEY is configured, calls go to the live Stitch API.
 * Otherwise falls back to a mock that returns sample data so the rest of
 * the stack (routes, UI, converter) can be developed and tested.
 */

export type DeviceType = 'DESKTOP' | 'MOBILE' | 'TABLET';

export interface StitchScreen {
  screenId: string;
  projectId: string;
  previewUrl: string;
  code: string;
}

export interface StitchProject {
  projectId: string;
  name: string;
}

