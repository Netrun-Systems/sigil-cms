let mockScreenCounter = 0;

function mockScreenId(): string {
  mockScreenCounter++;
  return `mock-screen-${Date.now()}-${mockScreenCounter}`;
}

// ---------------------------------------------------------------------------
// StitchService
// ---------------------------------------------------------------------------

export class StitchService {
  private apiKey: string | undefined;
  private baseUrl: string;
  private projectId: string | undefined;

  constructor(opts?: { apiKey?: string; baseUrl?: string; projectId?: string }) {
    this.apiKey = opts?.apiKey ?? process.env.STITCH_API_KEY;
    this.baseUrl = opts?.baseUrl ?? process.env.STITCH_BASE_URL ?? 'https://stitchapi.googleapis.com';
    this.projectId = opts?.projectId ?? process.env.STITCH_PROJECT_ID;
  }

  /** True when a real API key is configured. */
  get isConfigured(): boolean {
    return Boolean(this.apiKey);
  }

  // ── Projects ───────────────────────────────────────────────────────────

  async createProject(name: string): Promise<StitchProject> {
    if (!this.isConfigured) {
      const id = `mock-project-${Date.now()}`;
      return { projectId: id, name };
    }

    const res = await fetch(`${this.baseUrl}/v1/projects`, {
      method: 'POST',
      headers: this.headers(),
      body: JSON.stringify({ name }),
    });
    const data = await res.json();
    return { projectId: data.projectId ?? data.id, name: data.name ?? name };
  }

  // ── Screen generation ──────────────────────────────────────────────────

  async generateScreen(prompt: string, deviceType: DeviceType = 'DESKTOP'): Promise<StitchScreen> {
    if (!this.isConfigured) {
      const screenId = mockScreenId();
      return {
        screenId,
        projectId: this.projectId ?? 'mock-project',
        previewUrl: '',
        code: SAMPLE_HTML,
      };
    }

    const projectId = await this.ensureProject();
    const res = await fetch(`${this.baseUrl}/v1/projects/${projectId}/screens:generateFromText`, {
      method: 'POST',
      headers: this.headers(),
      body: JSON.stringify({ prompt, deviceType }),
    });
    const data = await res.json();
    return this.toScreen(data, projectId);
  }

  async editScreen(screenId: string, prompt: string): Promise<StitchScreen> {
    if (!this.isConfigured) {
      return {
        screenId,
        projectId: this.projectId ?? 'mock-project',
        previewUrl: '',
        code: SAMPLE_HTML,
      };
    }

    const projectId = await this.ensureProject();
    const res = await fetch(`${this.baseUrl}/v1/projects/${projectId}/screens:edit`, {
      method: 'POST',
      headers: this.headers(),
      body: JSON.stringify({ screenIds: [screenId], prompt }),
    });
    const data = await res.json();
    const screens = data.screens ?? [data];
    return this.toScreen(screens[0], projectId);
  }

  async getScreenCode(screenId: string): Promise<string> {
    if (!this.isConfigured) return SAMPLE_HTML;

    const projectId = await this.ensureProject();
    const res = await fetch(`${this.baseUrl}/v1/projects/${projectId}/screens/${screenId}/code`, {
      headers: this.headers(),
    });
    const data = await res.json();
    return data.html ?? data.code ?? '';
  }

  async getScreenImage(screenId: string): Promise<string> {
    if (!this.isConfigured) return '';

    const projectId = await this.ensureProject();
    const res = await fetch(`${this.baseUrl}/v1/projects/${projectId}/screens/${screenId}/image`, {
      headers: this.headers(),
    });
    const data = await res.json();
    return data.imageUrl ?? data.url ?? '';
  }

  async generateVariants(screenId: string, count = 3): Promise<StitchScreen[]> {
    if (!this.isConfigured) {
      return Array.from({ length: count }, () => ({
        screenId: mockScreenId(),
        projectId: this.projectId ?? 'mock-project',
        previewUrl: '',
        code: SAMPLE_HTML,
      }));
    }

    const projectId = await this.ensureProject();
    const res = await fetch(`${this.baseUrl}/v1/projects/${projectId}/screens/${screenId}:generateVariants`, {
      method: 'POST',
      headers: this.headers(),
      body: JSON.stringify({ count }),
    });
    const data = await res.json();
    const screens = data.variants ?? data.screens ?? [];
    return screens.map((s: Record<string, unknown>) => this.toScreen(s, projectId));
  }

  // ── Internals ──────────────────────────────────────────────────────────

  private headers(): Record<string, string> {
    return {
      'Content-Type': 'application/json',
      ...(this.apiKey ? { Authorization: `Bearer ${this.apiKey}` } : {}),
    };
  }

  private async ensureProject(): Promise<string> {
    if (this.projectId) return this.projectId;
    const project = await this.createProject('sigil-cms-designs');
    this.projectId = project.projectId;
    return this.projectId;
  }

  private toScreen(data: Record<string, unknown>, projectId: string): StitchScreen {
    return {
      screenId: (data.screenId ?? data.id ?? mockScreenId()) as string,
      projectId,
      previewUrl: (data.previewUrl ?? data.imageUrl ?? '') as string,
      code: (data.html ?? data.code ?? '') as string,
    };
  }
}
